bool is_white(char c) {
    return c == ' ' || c == '\t' || c == '\r';
}

void skip_spaces(const std::string &expression, size_t &pointer) {
    while (pointer < expression.length() && is_white(expression[pointer])) {
        ++pointer;
    }
}

