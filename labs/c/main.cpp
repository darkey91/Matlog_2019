class AST {
public :
    pNode root = nullptr;
    void parse() {
        root = impl();
        fillTree(root);
    }

    void setExpression(const std::string &expression) {
        this->expression = expression;
        init();
    }

private :
    void init() {
        cur_token = BEGIN;
        pointer = 0;
    }

    bool partOfName(char c) {
        return ('A' <= c && c <= 'Z') || ('0' <= c && c <= '9') || c == '\'';
    }

    // returns length of variable name and move pointer to the index of last letter in variable name
    size_t get_variable_name_len() {
        size_t start = 0;
        while (pointer < expression.length() && partOfName(expression[pointer])) {
            ++pointer;
            ++start;
        }
        return start;
    }

    //set spans of tree for each node and counts hash
    void fillTree(const pNode &node) {
        if (node == nullptr || (node->left == nullptr && node->right == nullptr))
            return;

        fillTree(node->left);
        fillTree(node->right);

        if (node->right == nullptr) {
            node->set_hash(node->getLeftChildHash() * P_left + '!');
        } else {
            node->set_hash(node->getLeftChildHash() * P_left + node->getRightChildHash() * P_right + node->value[0]);
            assert(node->value[0] == '-'
                   || node->value[0] == '&'
                   || node->value[0] == '|');
        }
    }

    void get_token() {
        skip_spaces(expression, pointer);
        if (pointer >= expression.length()) {
            cur_token = END;
            return;
        }

        char cur_symbol = expression[pointer];

        switch (cur_symbol) {
            case '-' : {
                ++pointer;
                cur_token = IMPL;
                break;
            }
            case '&' : {
                cur_token = AND;
                break;
            }
            case '|' : {
                cur_token = OR;
                break;
            }
            case '!' : {
                cur_token = NOT;
                break;
            }
            case '(' : {
                cur_token = OPEN;
                break;
            }
            case ')' : {
                cur_token = CLOSE;
                break;
            }
            default: {
                cur_token = VAR;
                size_t start = pointer;
                cur_var_name = expression.substr(start, get_variable_name_len());
                --pointer;
                break;
            }
        }
        ++pointer;
    }

    pNode ver() {
        get_token();
        pNode result;
        switch (cur_token) {
            case VAR : {
                result.reset(new Node(cur_var_name));
                result->set_hash(std::hash<std::string>()(cur_var_name));
                get_token();
                break;
            }
            case NOT : {
                pNode tmp(new Node("!", ver(), nullptr));
                result = std::move(tmp);
                break;
            }
            case OPEN : {
                result = impl();
                get_token();
                break;
            }
        }
        return result;
    }

    pNode conj() {
        pNode result = ver();
        while (true) {
            switch (cur_token) {
                case AND: {
                    pNode tmp(new Node("&", std::move(result), ver()));
                    result = std::move(tmp);
                    break;
                }
                default:
                    return result;
            }
        }
    }

    pNode disj() {
        pNode result = conj();
        while (true) {
            switch (cur_token) {
                case OR : {
                    pNode tmp(new Node("|", std::move(result), conj()));
                    result = std::move(tmp);
                    break;
                }
                default:
                    return result;
            }
        }
    }

    pNode impl() {
        pNode result = disj();
        while (true) {
            switch (cur_token) {
                case IMPL : {
                    pNode tmp(new Node("->", std::move(result), impl()));
                    result = std::move(tmp);
                    break;
                }
                default:
                    return result;
            }
        }
    }

    token cur_token = BEGIN;
    size_t pointer = 0;
    std::string cur_var_name;
    std::string expression;
};

